  Future<void> fetchAllEmployees() async {
    _isLoading = true;
    notifyListeners();

    await Future.delayed(const Duration(milliseconds: 200));

    _employees = rawEmployees.map((e) => _mapToModel(e)).toList();
    _isLoading = false;
    notifyListeners();
  }

