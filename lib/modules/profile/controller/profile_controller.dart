  Future<void> fetchEmployeeProfile(String employeeId) async {
    _isLoading = true;
    notifyListeners();

    await Future.delayed(const Duration(milliseconds: 200));

    final match = rawEmployees.firstWhere((e) => e['empNo'] == employeeId, orElse: () => rawEmployees.first);
    _employee = _mapToModel(match);
    _isLoading = false;
    notifyListeners();
  }

